package javalearning;

public class MathematicalOperators {

	public static void main(String[] args) {
		//define variables
		
		int firstValue = 15;
		int secondValue = 2;
		
		//addition
		int a = firstValue + secondValue;
		System.out.println("- Additon of firstValue and secondValue(15+2): "+a );

		 // subtraction
		int b = firstValue - secondValue;
		System.out.println("- Subtraction of firstValue and secondValue(15-2): "+b);
		
		// multiplication
		int c = firstValue * secondValue;
		System.out.println("- Multiplcation of firstValue and secondValue(15*2): "+c);
		
		// division
		int d = firstValue / secondValue;
		System.out.println("- Division of firstValue and secondValue(15/2): "+d);
		
		// modulo
		int e = firstValue + secondValue;
		System.out.println("- Modulo of firstValue and secondValue(15%2): "+e);
		

				
	}
	


	}


